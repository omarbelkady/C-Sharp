using System;
using System.Collections.Generic;
using System.Linq;

namespace oneToTen{
    class oneToTen{
        public static void RunMyProg()
        {
            for(int num=1;num<=10;num++)
            {
                Console.WriteLine(num);
            }
        }
        
        static void Main()
        {
            RunMyProg();
        }
    }
}
